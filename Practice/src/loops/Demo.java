package loops;

public class Demo 
{
	public static void main(String[] args) 
	{
		String str = "demo";
		
		System.out.print(str.charAt(0));
		
		for(int i=1;i<=2;i++)
		{
			System.out.print(str.charAt(1));
		}
		
		for(int i=1;i<=3;i++)
		{
			System.out.print(str.charAt(2));
		}
		
		for(int i=1;i<=4;i++)
		{
			System.out.print(str.charAt(3));
		}
	}
}
